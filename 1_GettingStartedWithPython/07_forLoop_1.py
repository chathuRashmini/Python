# FOR LOOP IN PYTHON

for i in [5, 4, 3, 2, 1]:
	# notice that no paranthesis are used here for the loop
    print(i)
print('End of loop 1')

frndz = ['Jacob', 'Edward', 'Bella'] # a list of strings

for frnd in frndz:
	# frnd stands for one element in the list in each iteration
    print (frnd)
print('End of loop 2')
