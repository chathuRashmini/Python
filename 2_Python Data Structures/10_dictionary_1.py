# PYTHON DICTIONARIES PART 01

purse = dict() #initialize a dictionary
purse['money'] = 12.50 # float
purse['candy'] = 'lollypop' # string
purse['tissue'] = 3 # integer
print(purse)

print('purse[candy]: ', purse['candy'])

bag = dict() # an emoty dictionary
print(bag)

# dictionaries have key value pairs
# they can store variables with different datatypes at the same time